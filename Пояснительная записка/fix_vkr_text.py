for idx, text in replacements.items():
    if idx < len(doc.paragraphs):
        doc.paragraphs[idx].text = text

caption_pat = re.compile(r"(Рисунок\s+)(\d+)")
ref_pat = re.compile(r"((?:рисунке|рисунок|рисунка)\s+)(\d+)", re.IGNORECASE)

for i, paragraph in enumerate(doc.paragraphs):
    if i < 90:
        continue
    text = paragraph.text
    if not text:
        continue
    if text.lstrip().startswith("Рисунок"):
        text = caption_pat.sub(lambda m: f"{m.group(1)}{int(m.group(2)) + 9}", text)
    else:
        text = ref_pat.sub(lambda m: f"{m.group(1)}{int(m.group(2)) + 9}", text)
    if text != paragraph.text:
        paragraph.text = text

cleanup_pairs = {
    "целесообразно показать": "представлено",
    "Место для такого снимка приведено": "Пример приведен",
    "Место для демонстрации этой страницы приведено": "Пример этой страницы приведен",
    "Место для снимка панели модерации приведено": "Экран панели модерации приведен",
    "должен быть виден": "отображается",
    "должны отображаться": "отображаются",
    "должен быть доступен": "доступен",
    "должна появиться": "появляется",
    "не должна участвовать": "не участвует",
    "должен появиться": "появляется",
    "комиссия сможет увидеть": "становится видно",
}

for paragraph in doc.paragraphs:
    text = paragraph.text
    new = text
    for old, new_value in cleanup_pairs.items():
        new = new.replace(old, new_value)
    if new != text:
        paragraph.text = new

doc.save(dst)
